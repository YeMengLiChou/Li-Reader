package cn.li.baselib.utils

/**
 *
 * */
object TimeUtils {

    const val SECOND_MILLIONS = 1000

    const val MINUTE_MILLIONS = 60 * SECOND_MILLIONS

    const val HOUR_MILLIONS = 60 * MINUTE_MILLIONS

    const val DAY_MILLIONS = 24 * HOUR_MILLIONS

    const val WEEK_MILLIONS = 7 * DAY_MILLIONS

    const val MONTH_MILLIONS = 30L * DAY_MILLIONS

    const val YEAR_MILLIONS = 365L * DAY_MILLIONS

    const val MINUTE_SECONDS = 60

    const val HOUR_SECONDS = 60 * MINUTE_SECONDS

    const val DAY_SECONDS = 24 * HOUR_SECONDS


}